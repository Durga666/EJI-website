<div class="container" >

	<h1 style="font-family: 'Yu Gothic Light',sans-serif;text-align: center;padding: 2%;" >WHISTLE BLOWER POLICY</h1>
	<br>	<br>
	<h2 class="subtitles" >Purpose</h2>
	<br>	

	<p class="contents" >

	As a part of the Company’s integrity, the Company follows the outlined whistle blower policy which is designed to allow the complainant / Whistle blower to give voice to his / her concerns without any fear of reprisal / victimisation from the superiors / management.</p>
	<br>	<br>
	<h2 class="subtitles" >Scope</h2>
	<br>

	<p class="contents" >The whistle blower can be any stakeholder, whether employee, dealer, vendor, customer, shareholder, contractors and their personnel and other connected persons, Provided, that such personnel do not have ongoing or anticipated claims or legal proceedings or any other litigation on the company. Further, all such complainants whose associates, relations, direct or indirect family members who have any such arbitration with the company are not eligible to complain under this policy.</p>
	<br>	<br> 	<br> 	<br>


	<h2 class="subtitles" >Coverage of the Policy</h2>
	<br>


	<p class="contents linespace" >The whistle blower may complain on the following areas only within the premises of the company.</p>

	<p class="contents linespace">1. Incorrect financial reporting.</p>

	<p class="contents linespace" >2. Moral standards not being maintained.</p>

	<p class="contents linespace" >3. Not in line with Company policy.</p>

	<p class="contents linespace" >4. Illegal in nature and substance.</p>

	<p class="contents linespace" >5. Improper conduct and conduct not becoming our company.</p>


	<br>
	<h2 class="subtitles">Procedure</h2>
	<br>

	<p class="contents linespace" >1. The Whistle Blower Policy (‘the Policy’) shall be administered by the Chairman cum Managing Director of the Company.</p>
	<p class="contents linespace" >2. The Company has assigned the e-mail ID of ks@ejindia.com on which the employee can report or send a written complaint to the Chairman cum Managing Director of the Company.</p>
	<p class="contents linespace" >3. The Whistle Blowing procedure is extended to be used for serious and sensitive issues and the compliant should be specific in nature containing sufficient details to permit investigation without need for direct contact.</p>
	<p class="contents linespace">The complaint shall include:</p>

	<p class="contents linespace" >* Nature and facts of the complaint</p>

	<p class="contents linespace" >* People involved</p>

	<p class="contents linespace">* The impact, Monetary or otherwise on the Company</p>

	<p class="contents linespace" >* Name, address/ work location of the Complainant</p>

	<p class="contents linespace" >* Supporting documents and any other evidence.</p>

	<p class="contents linespace" >4. Consistent with the Company’s philosophy, the Company would expect the complainants to put their names for investigation purposes. Anonymous applications will not be covered by the policy.</p>

	<p class="contents linespace" >5. The process of investigation may take the help of the Head of Internal Audit/ HR Department or any other person. The investigation will be completed within 60 days of the receipt of the complaint. Within 60 days the decision on the complaint will be communicated to the Whistle Blower by either the Chairman cum Managing Director.</p>

	<p class="contents linespace" >6. For any complaint, Chairman cum Managing Director may constitute a small Committee to assist them with the investigation.</p>

	<p class="contents linespace">7. Based on their findings as recommended to the Chairman cum Managing Director appropriate course of action will be taken.</p>

	<p class="contents linespace">8. Only in exceptional cases, where the complaint itself is against the Chairman cum Managing Director or when the complainant is not satisfied with the outcome of the investigation carried out by the Chairman cum Managing Director he can directly appeal to the Chairman of the Audit Committee. The Chairman of the Audit Committee will inform the Complainant the date, time and place where he/ she can meet. This will be communicated at least eight days in advance.</p>

	<p class="contents linespace" >9. The Committee deliberations shall be incorporated in the minutes and documented as part of the final report.</p>

	<p class="contents linespace" >10. The complaints received under this policy will be put forward, to the Chairman of the Audit Committee.</p>

	<br><br>
	<h2 class="subtitles">Confidentiality</h2>
	<br>


	 
		<p class="contents linespace"  >The Proceedings shall be carried out in strict confidentiality, in an unbiased
		manner and shall ensure thorough fact finding. The Whistle Blower-Complainant, Chairman cum Managing Director, Audit Committee Chairman and every
		internal and external stakeholder involved in the process shall:</p> 
		<p class="contents linespace"  >a. Maintain complete confidentiality / secrecy of the matter</p>
		<p class="contents linespace"  >b. Not discuss the matter in any informal / social gatherings / meetings</p>
		<p class="contents linespace"  >c. Not to keep papers unattended anywhere at any time</p>
		<p class="contents linespace"  >d. Keep the electronic mails / files under password</p>               
		<br>  
		<br>

		<h2 class="subtitles">Safeguarding the Interest of the Whistle Blower</h2>
		<br>

		<p class="contents linespace" style="line-height: 29px;" > The Company will not tolerate harassment or victimization and will take action to protect the whistle blower (Complainant). The Company assures every Whistle Blower, protection against unfair termination or any other disciplinary action or unlawful discrimination or retaliation in any manner for blowing the whistle under this policy. The Company will do its best to protect whistle blower’s identity when he/she makes a complaint. It must be appreciated, however, that the investigation process may require a statement by the whistle blower as part of the evidence.</p>

		<br>
		<h2 class="subtitles" >Malicious Allegations</h2>
		<br>
		<p  class="contents"  style="line-height: 29px;" >The intent of the policy is to bring genuine and serious issues to the core. Allegations based not on reality but due to malicious intent will result in serious consequences and strong disciplinary action against the complainant concerned.</p>


