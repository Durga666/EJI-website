	<p class="contents" >

	Socially responsible business practice is an integral part of an organization’s effort at ensuring good Corporate Governance. Corporate Social Responsibility (CSR) is therefore a tool through which an organization reflects and pledges its commitment to support and participate in community building efforts.</p>
	<br>	<br>
	<p class="contents">
		The Government of India has vide section 135 of the Companies Act, 2013 (the Act) and the Companies (Corporate Social Responsibility Policy) Rules, 2014 (the Rules) has brought out CSR guidelines to be adhered to by certain class of companies.
	</p>
	<br>	<br>
	<p class="contents">
		As an organization, Emerald Jewel Industry India Limited is covered by these rules. In this regard it has to be noted that EJIIL since inception has been in the vanguard of society welfare and development through numerous initiatives undertaken on a regular basis.
