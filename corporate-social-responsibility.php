	<h2 class="subtitles">GEOGRAPHICAL AREA</h2>
		<br>

		<p class="contents linespace" style="line-height: 29px;" > EJIIL may undertake CSR activities anywhere in India; however preference may be accorded for the geographical locations (district, city / town or village) where the Company’s operations are located.</p>

		<br><br>


		<h2 class="subtitles">AMOUNT TO BE SPENT</h2>
		<br>

		<p class="contents linespace" style="line-height: 29px;" >During every financial year the CSR Committee shall arrange to spend for the specified CSR activities, an amount which is not less than 2 % of the average net profits of the immediately preceding three financial years. However in any financial year, for any reasons, the company is unable to spend an amount as stated above, shall provide suitable explanation stating the reasons therefor to the Board of Directors and the Board shall include the same in the Directors ’ Report of the Company.</p>

		<br><br>



<h2 class="subtitles">CSR EXPENDITURE BUDGET</h2>
		<br>
		<p class="contents linespace" style="line-height: 29px;" > A CSR expenditure budget (budget) for every financial year shall be prepared and submitted to the CSR committee for recommendation. The budget shall provide details as to the proposed activity wise expenditure and the time of expenditure spread over the financial year. The CSR Committee after deliberations may recommend with or without modification to the Board of Directors for approval at its first meeting to be held in each financial year. Then the company shall allocate funds as per the Board’s approval.</p>

		<br><br>



<h2 class="subtitles">METHOD OF CSR EXPENDITURE</h2>
		<br>
		<p class="contents linespace" style="line-height: 29px;" >The Company may undertake the CSR activities directly by itself and by Krisha Charitable Trust or by contributing to external agencies / Implementing Agencies, like established Charitable Trusts of long standing reputation or partly by it and partly through such Trusts as may be recommended by the CSR Committee and approved by the Board.</p>
		<br>
		<p class="contents linespace" style="line-height: 29px;" >Where contributions are made to an external Trust, the company shall obtain a Fund Utilization Certificate duly certified by an independent Chartered Accountant and submitted to the CSR Committee. For ongoing projects, extending beyond the financial year, interim status report on the progress of the project duly certified by an independent Chartered Accountant shall be obtained.</p>

		<br><br>

		<h2 class="subtitles">CSR REPORTING</h2>
		<br>
		<p class="contents linespace" style="line-height: 29px;" >The Company shall submit to the CSR Committee the details of projects / programmes / activities undertaken periodically at reasonable intervals. The CSR committee shall forward a report on CSR projects / programmes / activities of the Company to the Board of Directors once in a year. The Board shall provide a report in the prescribed format on CSR activities of theCompany in its Report to shareholders every year.</p>

		<br><br>
		<h2 class="subtitles">POLICY OPERATION AND REVIEW</h2>
