	<p class="contents">
		Considering the new statutory requirement EJIIL has framed a CSR policy that is reflective of its long-held beliefs and commitment towards community development. The CSR Policy of EJIIL contains principles that will guide future CSR projects / Programmes / activities, such as prescribing the budgetary limits, scope of CSR activities, the geography within which the activities are to be carried out, procedure for sanction of funds, procedure for obtaining Board approval, monitoring mechanism for projects, programmes and activities etc.
	</p>
	<br>	<br>


	<h2 class="subtitles" >DEFINITIONS</h2>
	<br>

	<p class="contents" >1. CSR Committee: means a Committee of Board of Directors, consisting of three Directors at least one of whom shall be an independent Director, duly constituted by the Board of Directors of the Company. The CSR Committee shall be headed by a Chairman duly elected as per the provisions of the Act.</p>
	<br>	<br>

	<p class="contents" >2.Net profit : means profit calculated as per section 198 of the Companies Act, 2013 and shown in the audited annual financial statements of the company but does not include profit earned through overseas operations, either by a separate company or otherwise, and dividend received from other companies in India.</p>
	<br>	<br> 

	<p class="contents" >3. CSR expenditure: means any amount spent by the Company within the territory of India, in any financial year for the projects / programmes / activities covered under items listed in Schedule VII of the Act.</p>
	<br>	<br> 	<br> 	<br>


	<h2 class="subtitles" >SCOPE</h2>
	<br>


	<p class="contents" >Scope of CSR policy is to lay down the guiding principles to enable the CSR Committee and the Board of Directors to determine the amount of money to be earmarked for the CSR Expenditure in any financial year, the projects / programmes / activities for which the amount has to be spent and allocation of funds among various heads of CSR expenditure the geographical areas where the CSR activities are to be undertaken, priority for certain CSR activities over other CSR activities, timing of the expenditure, periodicity of reporting and to undertake any other activity required for due compliance of the provisions of the Act and the Rules.</p>

	<br><br>
	<h2 class="subtitles">CSR ACTIVITIES TO BE COVERED</h2>
	<br>

	<p class="contents" >CSR activity to be undertaken shall be one or more of the projects / programmes /activities specified under the Schedule VII of the Act. The CSR committee may recommend to the company the nature of the CSR activity to be undertaken for each financial year. The CSR committee is at liberty to identify the projects / programmes /activities based on the needs of the society. The Administration Department / Public Relations Department / Unit Heads of the Company may submit to the Chairman of the CSR Committee the nature of CSR activities to be pursued from time to time based on the requirements of the area in which the factories / establishments of the Company are situated.</p>
	<br><br>


