          <p class="contents" >The Director, KMP and Senior Management Personnel shall retire as per the
applicable provisions of the Act and the prevailing policy of the Company. The
Board will have the discretion to retain the Director, KMP, Senior Management
Personnel in the same position/ remuneration or otherwise even after
attaining the retirement age, for the benefit of the Company.</p>
<p class="contents" ><b>Policy For Remuneration to Directors/KMP/Senior Management Personnel</b></p> 
<p class="contents" ><b>i. Remuneration to Managing Director / Whole-time Directors:</b></p>
<p class="contents" >a) The Remuneration/ Commission etc. to be paid to Managing Director /
Whole-time Directors, etc. shall be governed as per provisions of the
Companies Act, 2013 and rules made there under or any other enactment for
the time being in force and the approvals obtained from the Members of the
Company.</p>
<p class="contents" >b) The Nomination and Remuneration Committee shall make such
recommendations to the Board of Directors, as it may consider appropriate
with regard to remuneration to Managing Director / Whole-time Directors.</p>
<p class="contents" ><b>ii. Remuneration to Non- Executive / Independent Directors:</b></p>
<p class="contents" >a) The Non-Executive / Independent Directors may receive sitting fees and
such other remuneration as permissible under the provisions of Companies
Act, 2013 [Subject to the approval of the Board of Directors].The amount of
sitting fees shall be such as may be recommended by the Nomination and
Remuneration Committee and approved by the Board of Directors.</p>          
<p class="contents" >b) All the remuneration of the Non- Executive / Independent Directors
(excluding remuneration for attending meetings as prescribed under Section
197 (5) of the Companies Act, 2013) shall be subject to ceiling/ limits as
provided under Companies Act, 2013 and rules made there under or any other
enactment for the time being in force. The amount of such remuneration shall
be such as may be recommended by the Nomination and Remuneration
Committee and approved by the Board of Directors or shareholders, as the
case may be.</p>
<p class="contents"  >c) An Independent Director shall not be eligible to get Stock Options and also
shall not be eligible to anticipate in any share based payment schemes of the
Company.</p>
<p class="contents" >d) Any remuneration paid to Non- Executive / Independent Directors for
services rendered which are of professional in nature shall not be considered
as part of the remuneration for the purposes of clause (b) above if the following
conditions are satisfied:</p>
<p class="contents" >i. The Services are rendered by such Director in his capacity as the professional; and</p>
<p class="contents" >ii. In the opinion of the Committee, the director possesses the requisite qualification for the practice of that profession.</p>
<p class="contents" >iii. Remuneration to Key Managerial Personnel and Senior Management:</p>
<p class="contents" >1. The remuneration to Key Managerial Personnel and Senior Management
shall consist of fixed pay and incentive pay, in compliance with the provisions
of the Companies Act, 2013 and in accordance with the Company’s Policy.</p>
<p class="contents" >2. The Compensation Committee of the Company, constituted for the purpose
of administering the Employee Stock Option/ Purchase Schemes, shall
determine the stock options and other share based payments to be made to
Key Managerial Personnel and Senior Management.</p>
<p class="contents" >3. The Fixed pay shall include monthly remuneration, employer’s contribution
to Provident Fund, contribution to pension fund, pension schemes, etc. as
decided from to time.</p>
<p class="contents" >4. The Incentive pay shall be decided based on the balance between
performance of the Company and performance of the Key Managerial
Personnel and Senior Management, to be decided annually or at such
intervals as may be considered appropriate. Implementation The Committee
may issue guidelines, procedures, formats, reporting mechanism and
manuals in supplement and for better implementation of this policy as
considered appropriate. The Committee may Delegate any of its powers to one
or more of its members.</p>
