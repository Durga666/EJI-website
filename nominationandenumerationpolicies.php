	<h2 class="subtitles" ><b>Appointment and Removal Of Director, Key Managerial Personnel And Senior Management</b></h2>

	<br>

          <p class="contents" >The Proceedings shall be carried out in strict confidentiality, in an unbiased
manner and shall ensure thorough fact finding. The Whistle Blower-Complainant,
Managing Director, Audit Committee Chairman and every internal and external
stakeholder involved in the process shall:</p>
<p class="contents" >1. The Committee shall identify and ascertain the integrity, qualification, expertise and experience of the person for appointment as Director, KMP or
at Senior Management level and recommend his / her appointment, as per
Company’s Policy.</p>
<p class="contents" >2. A person should possess adequate qualification, expertise and experience for the position he / she is considered for appointment. The Committee has
authority to decide whether qualification, expertise and experience possessed by a person is sufficient/satisfactory for the position.</p>
<p class="contents" >3. The Company shall not appoint or continue the employment of any person as Whole-time Director who has attained the age of seventy years. Provided
that the term of the person holding this position may be extended beyond the age of seventy years with the approval of shareholders by passing a special
resolution.</p>
             
