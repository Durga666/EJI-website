		  <div class="clearfix"></div>
			 <div class="title"><h2 class="subtitles" ><b>Independent Director</b></h2>

          <p class="contents" >An Independent Director shall hold office for a term up to five consecutive
years on the Board of the Company and will be eligible for re-appointment on
passing of a special resolution by the Company and disclosure of such
appointment in the Board's report. No Independent Director shall hold office
for more than two consecutive terms of upto maximum of 5 years each, but
such Independent Director shall be eligible for appointment after expiry of
three years of ceasing to become an Independent Director.
Provided that an Independent Director shall not, during the said period of
three years, be appointed in or be associated with the Company in any other
capacity, either directly or indirectly.</p>
<p class="contents" >At the time of appointment of Independent Director it should be ensured that
number of Boards on which such Independent Director serves is restricted to
seven listed companies as an Independent Director and three listed
companies as an Independent Director in case such person is serving as a
Whole-time Director of a listed company or such other number as may be
prescribed under the Act. Evaluation The Committee shall carry out
evaluation of performance of Director, KMP and Senior Management
Personnel yearly or at such intervals as may be considered necessary.</p>              
